const Spinner: React.FC<{ size?: 'sm' | 'md' | 'lg' }> = ({ size = 'md' }) => {
  const sizes = {
    sm: 'h-4 w-4',
    md: 'h-6 w-6',
    lg: 'h-8 w-8'
  };
  
  return (
    <div className={`${sizes[size]} border-2 border-white border-t-transparent rounded-full animate-spin`}></div>
  );
};

export default Spinner;
